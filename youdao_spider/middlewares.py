    def process_exception(self, request, exception, spider):
        # Called when a download handler or a process_request()
        # (from other downloader middleware) raises an exception.

        # Must either:
        # - return None: continue processing this exception
        # - return a Response object: stops process_exception() chain
        # - return a Request object: stops process_exception() chain
        cur_proxy = request.meta.get('proxy')  # 取出当前代理
        id_num=request.meta.get('id_num')
        from twisted.internet.error import ConnectionRefusedError, TimeoutError ,TCPTimedOutError
        from scrapy.downloadermiddlewares.retry import RetryMiddleware


        # 如果本次请求使用了代理，并且网络请求报错，认为这个ip出了问题
        if cur_proxy and isinstance(exception, (ConnectionRefusedError, TimeoutError ,RetryMiddleware ,TCPTimedOutError)):
            print("******************err:{} id_num:{} IP:{} ".format(exception,id_num ,cur_proxy))
            del request.meta['proxy']
            # 重新下载这个请求
            self.__get_ip()
            if 'err' in self.proxy:
                return request
            else:
                request.meta['proxy'] = self.proxy
                return request
        else:
            return None

